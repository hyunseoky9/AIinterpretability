    def reset(self):
        self.obs = np.zeros(np.sum(self.statevar_dim))
        self.state = np.zeros(np.sum(self.obsvar_dim))

        X = np.random.choice([0,1], size=self.X_dim, p=[0.3,0.7]) # initial occupancy state
        H = np.random.choice([0,1], size=self.H_dim, p=[self.deltaH, 1-self.deltaH]) # initial habitat quality
        Z = np.random.choice([0,1], size=self.Z_dim, p=[self.Ztrans[0,0], self.Ztrans[1,0]]) # initial dispersal regime
        if self.partial_observability == 1:
            M, Y = self.survey(X) # initial survey observation
            if self.dispersal_regime == 1:
                self.state = np.concatenate([X,H,M,Z,[0]])
            else: 
                self.state = np.concatenate([X,H,M,[0]])
            self.obs = np.concatenate([Y,H,M,[0]])
        else:
            if self.dispersal_regime == 1:
                self.state = np.concatenate([X,H,Z,[0]])
                self.obs = np.concatenate([X,H,Z,[0]])
            else:
                self.state = np.concatenate([X,H,[0]])
                self.obs = np.concatenate([X,H,[0]])
        if self.dim2state == 1:
            self.state, self.obs = self.change_to_2D(connectivity=self.w @ X, Z_next=Z if self.dispersal_regime == 1 else None, t=0)
        return self.obs, self.state
    
    def step(self, action):
        info = {}
        if self.action_portfolio == 0:
            aR = action[self.aidx['aR']]
            aS = action[self.aidx['aS']]
        elif self.action_portfolio == 1:
            aR = action[self.aidx['aR']]
            aS = np.zeros(self.aS_dim, dtype=int)
        elif self.action_portfolio == 2:
            aR = np.zeros(self.aR_dim, dtype=int)
            aS = action[self.aidx['aS']]
        aY = action[self.aidx['aY']] if self.survey_is_action == 1 else np.zeros(self.aY_dim, dtype=int)
        if self.dim2state == 0:
            X = self.state[self.sidx['X']]
            H = self.state[self.sidx['H']]
            if self.dispersal_regime == 1:
                Z = self.state[self.sidx['Z']][0]
            else:
                Z = 0
            t = self.state[self.sidx['t']][0]
        else:
            X = self.state[:,self.sidx['X']]
            H = self.state[:,self.sidx['H']]
            if self.dispersal_regime == 1:
                Z = int(self.state[0,self.sidx['Z']])
            else:
                Z = 0
            t = self.state[0,self.sidx['t']]
        # calculate colonization and extinction probabilities based on current state and parameters
        connectivity = self.w @ X # connectivity for each patch based on current occupancy and dispersal weights
        cprob = 1 - np.exp(-self.alph0 * connectivity * (1 + self.alphZ * Z) * (1 + self.alphH * H))
        eprob = (1/(1+np.exp(-(self.beta0 - self.betaH * H)))) * (1 - cprob)
        # update state based on actions and transition probabilities
        ## occupancy 
        X_next = np.zeros(self.X_dim, dtype=int)
        mask = X==0
        mask2 = ~mask
        X_next[mask] = np.random.binomial(1, cprob[mask]) + aS[mask] * np.random.binomial(1, self.qs, size=np.sum(mask))
        X_next[mask2] = np.random.binomial(1, 1 - eprob[mask2]) + aS[mask2] * np.random.binomial(1, self.qs, size=np.sum(mask2))
        X_next = np.minimum(X_next, 1)
        ## habitat quality
        H_next = np.zeros(self.H_dim, dtype=int)
        H_next[aR==1] = 1
        mask = (aR == 0) & (H == 1)
        H_next[mask] = np.random.choice([0,1], size=np.sum(mask), p=[self.deltaH, 1-self.deltaH])
        ## dispersal regime
        if self.dispersal_regime == 1:
            Z_next = np.random.choice([0,1], size=self.Z_dim, p=self.Ztrans[:,Z])
        
        # update observaiton based on new state and observation probabilities
        if self.partial_observability == 1:
            M_next, Y_next = self.survey(X_next) if self.survey_is_action == 0 else self.survey(X_next, survey_patches=aY)
            self.state = np.concatenate([X_next, H_next, M_next])
            self.obs = np.concatenate([Y_next, H_next, M_next])            
            if self.dispersal_regime == 1:
                self.state = np.concatenate([self.state, Z_next])
            self.state = np.concatenate([self.state, [t+1]])
            self.obs = np.concatenate([self.obs, [t+1]])
        else:
            self.state = np.concatenate([X_next, H_next])
            self.obs = np.concatenate([X_next, H_next])
            if self.dispersal_regime == 1:
                self.state = np.concatenate([self.state, Z_next, [t+1]])
                self.obs = np.concatenate([self.obs, Z_next, [t+1]])
            else:
                self.state = np.concatenate([self.state, [t+1]])
                self.obs = np.concatenate([self.obs, [t+1]])
        if self.dim2state == 1: 
            new_connectivity = self.w @ X_next
            self.state, self.obs = self.change_to_2D(connectivity=new_connectivity, Z_next=Z_next if self.dispersal_regime == 1 else None, t=t+1)
        # reward
        reward = 1/self.patchnum * np.sum(X_next) - self.cr * np.sum(aR) - self.cs * np.sum(aS) # reward based on number of occupied patches and cost of actions
        # done
        if t == self.T-1:
            reward = reward - self.L * (np.sum(1 - X_next)) if self.terminal_penalty == 1 else reward # add terminal penalty
            done = True
        elif np.sum(X_next) == 0: # all patches are extinct
            done = True
        else:           
            done = False
        info['connectivity'] = connectivity
        info['extinction_prob'] = eprob
        info['colonization_prob'] = cprob
        info['occupied_fraction'] = np.mean(X_next)
        info['goodhabitat_fraction'] = np.mean(H_next)
        info['total_cost'] = self.cr * np.sum(aR) + self.cs * np.sum(aS)
        info['total_benefit'] = 1/self.patchnum * np.sum(X_next)
        return self.obs, reward, done, info

    def change_to_2D(self, connectivity=None, Z_next=None, t=None):
        new_state = np.zeros((self.patchnum, self.statefeature_dim))
        new_obs = np.zeros((self.patchnum, self.obsfeature_dim))
        if self.partial_observability == 1:
            new_state[:,:3] = np.array([self.state[:self.patchnum], self.state[self.patchnum:2*self.patchnum], self.state[2*self.patchnum:3*self.patchnum]]).T
            new_obs[:,:3] = np.array([self.obs[:self.patchnum], self.obs[self.patchnum:2*self.patchnum], self.obs[2*self.patchnum:3*self.patchnum]]).T
            idx = 3 
        else:
            new_state[:,:2] = np.array([self.state[:self.patchnum], self.state[self.patchnum:2*self.patchnum]]).T
            new_obs[:,:2] = np.array([self.obs[:self.patchnum], self.obs[self.patchnum:2*self.patchnum]]).T
            idx = 2
        if self.dim2state_with_C == 1:
            new_state[:,idx] = connectivity
            new_obs[:,idx] = connectivity
            idx += 1
        if self.dispersal_regime == 1:
            new_state[:,idx] = Z_next
            if self.partial_observability == 0:
                new_obs[:,idx] = Z_next
        new_state[:,-1] = t
        new_obs[:,-1] = t
        return new_state, new_obs
        
    def survey(self,X, survey_patches = None):
        M = np.zeros(self.M_dim, dtype=int)
        if self.survey_is_action == 1:
            ones_indices = survey_patches == 1
        else:
            ones_indices = np.random.choice(self.M_dim, size=int(self.B), replace=False)
        M[ones_indices] = 1
        Y = np.zeros(self.Y_dim, dtype=int)
        Y[M==1] = X[M==1] * np.random.choice([1,0], size=np.sum(M), p=[self.pdet, 1-self.pdet]) + (1-X[M==1]) * np.random.choice([1,0], size=np.sum(M), p=[self.pfp, 1-self.pfp]) # initial survey observation
        return M, Y 

    def heuristic_action(self):
        '''
        heuristic rule for no dispersal regime, no partial observability, and no terminal penalty.
        * Prioritize patches with higher incoming dispersal weight from all patches
        * no action on the last 2 steps if patch abundance is 10 or 20; no action on the last step if patch abundance is 5.
        - if extinct and bad habitat: restore and supplement
        - if extinct and good habitat and incoming dispersal weight from persisting patches is below w^bar_1: supplement only
        - if persisting and bad habitat and incoming dispersal weight from persisting patches is below w^bar_2: restore and supplement
        - if persisting and bad habitat and incoming dispersal weight from persisting patches is equal or above w^bar_2: restore only
        - if persisting and good habitat: no action
        '''

        if self.dim2state == 1:
            X = self.obs[:, self.oidx["X"]]
            H = self.obs[:, self.oidx["H"]]
            t = int(self.obs[0, self.oidx["t"]])
        else:
            X = self.obs[self.oidx["X"]]
            H = self.obs[self.oidx["H"]]
            t = int(self.obs[self.oidx["t"]][0])
        if self.dispersal_regime == 1:
            Z = self.obs[0,self.oidx["Z"]] if self.dim2state == 1 else self.obs[self.oidx["Z"]][0]
        else: 
            Z = 0
        
        # set wbar1 and wbar2 thresholds for heuristic rule based on settingID            
        if self.settingID == 18: # n=5
            wbar1 = 1.6
            wbar2 = 1.6
        elif self.settingID == 20: # n=10 median centrality
            wbar1 = 2.0
            wbar2 = 2.0
        elif self.settingID == 22: # n=10 low centrality
            wbar1 = 1.6
            wbar2 = 1.6
        elif self.settingID == 23: # n=10 high centrality
            wbar1 = 1.6
            wbar2 = 1.6
        elif self.settingID == 21: # n=20 
            wbar1 = 1.8
            wbar2 = 1.8
        
        incoming_w_occupied = np.matmul(self.w, X) # incoming dispersal weight from occupied patches

        # assign actions based on heuristic rule
        noactiontime = self.T - 2 if self.patchnum in [10,20] else self.T - 1
        if t >= noactiontime : # no action on the last few steps
            return np.zeros(self.actionspace_dim, dtype=int)
        else:
            ridx = []
            sidx = []
            aR = np.zeros(self.aR_dim, dtype=int)
            aS = np.zeros(self.aS_dim, dtype=int)
            bothactionidx = []
            for i in range(self.patchnum):
                if X[i] == 0 and H[i] == 0: # extinct and bad habitat
                    ridx.append(i)
                    sidx.append(i)
                    bothactionidx.append(i)
                elif X[i] == 0 and H[i] == 1 and incoming_w_occupied[i] <= wbar1: # extinct and good habitat and low incoming dispersal weight
                    sidx.append(i)
                elif X[i] == 1 and H[i] == 0 and incoming_w_occupied[i] <= wbar2: # persisting and bad habitat and low incoming dispersal weight
                    ridx.append(i)
                    sidx.append(i)
                    bothactionidx.append(i)
                elif X[i] == 1 and H[i] == 0 and incoming_w_occupied[i] >= wbar2: # persisting and bad habitat and high incoming dispersal weight
                    ridx.append(i)
            # sort by incoming dispersal weight and cut by self.kR and self.kS
            
            newridx = sorted(ridx, key=lambda x: self.incoming_w[x], reverse=True)[:self.kR] if len(ridx) > self.kR else ridx
            newsidx = sorted(sidx, key=lambda x: self.incoming_w[x], reverse=True)[:self.kS] if len(sidx) > self.kS else sidx
            # if patches that have both actions get taken off of one of the actions, they
            # should get taken off of the other action as well.
            for i in bothactionidx:
                if i in ridx and i not in newridx:
                    if i in newsidx:
                        newsidx.remove(i)
                elif i in sidx and i not in newsidx:
                    if i in newridx:
                        newridx.remove(i)
            # create action vector
            if len(ridx) > 0:
                aR[np.array(newridx)] = 1
            if len(sidx) > 0:
                aS[np.array(newsidx)] = 1
            action = np.concatenate([aR, aS])
            return action

